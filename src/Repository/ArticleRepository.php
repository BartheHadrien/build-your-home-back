/**
 * @extends ServiceEntityRepository<Article>
 *
 * @method Article|null find($id, $lockMode = null, $lockVersion = null)
 * @method Article|null findOneBy(array $criteria, array $orderBy = null)
 * @method Article[]    findAll()
 * @method Article[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class ArticleRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Article::class);
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function add(Article $entity, bool $flush = true): void
    {
        $this->_em->persist($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function remove(Article $entity, bool $flush = true): void
    {
        $this->_em->remove($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    /**
     * return array of all article(array) where stock < quantity ordered
     *
     * @return array
     */
    public function findEmptyStock(): array
    {
        // On se connecte à la DB 
        $dbal = $this->getEntityManager()->getConnection();
        // On prépare la requete sql
        $sql = "SELECT SUM(quantity) AS somme, `article_id`, `article`.*
                FROM `orderlist`
                INNER JOIN `article` ON `article`.`id` = `orderlist`.`article_id`
                GROUP BY `article_id`
                HAVING `article`.`stock` < somme";
        
        $stmt = $dbal->prepare($sql);
        $resultSet = $stmt->executeQuery();

        return $resultSet->fetchAllAssociative();
    }

